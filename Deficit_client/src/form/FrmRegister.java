    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String firstName = txtName.getText().trim();
        String lastName = txtLastName.getText().trim();
        String username = txtUsername.getText().trim();
        String password = String.copyValueOf(txtPassword.getPassword());
        String passwordAgain = String.copyValueOf(txtPasswordAgain.getPassword());
        
        setLabels();
        checkInput(firstName, lastName, username, password, passwordAgain);
        
        if(firstName.isEmpty() || lastName.isEmpty() || username.isEmpty() || password.isEmpty() || passwordAgain.isEmpty()){
            JOptionPane.showMessageDialog(this, "Morate uneti sva polja!");
            return;
        }
        
        
        
    }//GEN-LAST:event_btnRegisterActionPerformed

