    private void setLabels() {
        lblErrorFirstName.setText("");
        lblErrorLastName.setText("");
        lblErrorPassword.setText("");
        lblErrorPasswordAgain.setText("");
        lblErrorUsername.setText("");     
    }

    private void checkInput(String firstName, String lastName, String username, String password, String passwordAgain) {
        
        if(!password.equals(passwordAgain)){
            JOptionPane.showMessageDialog(this, "Lozinke se ne podudaraju!");
            return;
        }
        
        if(firstName.isEmpty())
            lblErrorFirstName.setText("Morate uneti ime");
        else
            lblErrorFirstName.setText("");
        if(lastName.isEmpty())
            lblErrorLastName.setText("Morate uneti prezime");
        else
            lblErrorLastName.setText("");
        if(username.isEmpty())
            lblErrorUsername.setText("Morate uneti korisničko ime");
        else
            lblErrorUsername.setText("");
        if(password.isEmpty())
            lblErrorPassword.setText("Morate uneti lozinku");
        else
            lblErrorPassword.setText("");
        if(passwordAgain.isEmpty())
            lblErrorPasswordAgain.setText("Morate ponoviti lozinku");
        else
            lblErrorPasswordAgain.setText("");    
    }
